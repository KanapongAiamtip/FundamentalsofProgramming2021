public class Printanarrayofstrings
{
    public static void main(String[] args) 
    {
        String[] names = {"Anna", "Beryl", "Clara", "Desmond", "Eleanor", "Fiona", "George", "Henry"};
        for (int i = 0; i < names.length; i++) 
        {
            System.out.println(names[i]);    
        }
    }
}
